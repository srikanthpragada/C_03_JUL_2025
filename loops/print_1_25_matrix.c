// Program to print 1 to 25 as a matrix
// Date : 15-JUL-2025

#include <stdio.h>

void main()
{
  int i,j, n = 1;


      for(i = 1; i <= 5; i ++)
      {
          for(j = 1; j <= 5; j ++)
          {
              printf("%5d ", n);
              n ++;
          }

          printf("\n");
      }





}
